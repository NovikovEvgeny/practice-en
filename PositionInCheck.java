
public class PositionInCheck {
	
	private int deviceID;
	private int count;
	
	public PositionInCheck(int deviceID, int count) {
		this.deviceID = deviceID;
		this.count = count;
	}
	
	public int getDeviceID() {
		return deviceID;
	}
	
	public int getCount() {
		return count;
	}
}
